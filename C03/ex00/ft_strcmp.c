#include <stdio.h>

int		ft_strcmp(char *s1, char *s2)
{
	char	c1;
	char	c2;
	int		i;

	i = 0;
	c1 = s1[i];
	c2 = s2[i];
	while (c1 == c2 && c1 != '\0' && c2 != '\0')
	{
		i++;
		c1 = s1[i];
		c2 = s2[i];
	}
	return (s1[i] - s2[i]);
}

int main()
{
	printf("%d\n", ft_strcmp("csd", "b"));
}